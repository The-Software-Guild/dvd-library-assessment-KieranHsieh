package com.kieran.dvd_library.ui;

/**
 * An enumerated value representing a selection in the DVD Library application's menu
 */
public enum EMenuSelection {
    /** Add a DVD */
    ADD,
    /** Remove a DVD */
    REMOVE,
    /** Edit a DVD */
    EDIT,
    /** List all DVDs */
    LIST_ALL,
    /** Retrieve a specific DVD by title */
    GET,
    /** Exit the application */
    EXIT
}
